from __future__ import annotations
"""MASPlot — plotnine-based base class for all MAS-Lab scientific figures.

Every figure that can be autogenerated in a pipeline, displayed in a
Jupyter notebook, or embedded in an interactive article should subclass
``MASPlot`` and implement :meth:`build`.

Three rendering *targets* are supported, each with a matching theme preset:

  ``paper``   — 8 pt sans-serif, print-ready (acmart single-col = 3.33 in,
                double-col = 6.97 in), Okabe–Ito palette
  ``slides``  — 14 pt, high-contrast white background, suitable for beamer
  ``web``     — 11 pt, SVG-embeddable in HTML articles, more padding

Quick start
-----------
::

    import pandas as pd
    from mas.lab.plots.mas_plot import MASPlot
    from plotnine import ggplot, aes, geom_col, geom_errorbar, labs

    class QualityBarPlot(MASPlot):
        plot_type    = "quality_bar"
        default_width  = 3.33
        default_height = 2.5

        def build(self, data=None) -> "ggplot":
            df = data if data is not None else self.data
            return (
                ggplot(df, aes(x="source", y="gsr", fill="source")) +
                geom_col(width=0.5) +
                geom_errorbar(aes(ymin="gsr_lo", ymax="gsr_hi"), width=0.15) +
                labs(title="Context source quality") +
                self.theme() +
                self.scale_fill()
            )

    # Notebook / script
    plot = QualityBarPlot(df, target="paper")
    paths = plot.save("figures/", stem="quality_bar")  # → ["quality_bar.svg", "quality_bar.pdf"]
    step  = plot.to_step("quality_bar", depends_on=["analysis"])  # → PipelineStep

Color palette
-------------
Okabe–Ito (Nature Methods 2008) is the default colorblind-safe palette.
It covers the three most common forms of colour-vision deficiency and
passes WCAG AA contrast at standard academic font sizes.

The eight canonical colors are exposed as :data:`OI_COLORS` (list) and
:data:`OI` (name → hex dict).  Semantic aliases are also provided::

    C_SA   = OI["blue"]    # single-agent / primary series
    C_MAS  = OI["vermil"]  # multi-agent topology
    C_OK   = OI["green"]   # satisfied / positive
    C_TRIG = OI["orange"]  # triggered / secondary

Use :meth:`MASPlot.scale_fill` and :meth:`MASPlot.scale_color` to apply
the palette automatically.
"""


import math
import warnings
from abc import ABC, abstractmethod
from pathlib import Path
from typing import TYPE_CHECKING, Any, Literal

if TYPE_CHECKING:
    from plotnine import ggplot


# ---------------------------------------------------------------------------
# Okabe–Ito palette  (Nature Methods 2008)
# Order: perceptual distinctiveness within each color-vision deficiency model.
# ---------------------------------------------------------------------------

OI_COLORS: list[str] = [
    "#0072B2",   # 0  blue          single-agent / primary
    "#D55E00",   # 1  vermilion     multi-agent / MAS
    "#009E73",   # 2  green         OK / positive
    "#E69F00",   # 3  orange        triggered / secondary
    "#56B4E9",   # 4  sky blue      tool-based
    "#CC79A7",   # 5  reddish-purple memory / contextual
    "#F0E442",   # 6  yellow        caution (never use alone)
    "#000000",   # 7  black         baseline / reference
]

OI: dict[str, str] = {
    "blue":    OI_COLORS[0],
    "vermil":  OI_COLORS[1],
    "green":   OI_COLORS[2],
    "orange":  OI_COLORS[3],
    "sky":     OI_COLORS[4],
    "purple":  OI_COLORS[5],
    "yellow":  OI_COLORS[6],
    "black":   OI_COLORS[7],
    "grey":    "#999999",
    "lgrey":   "#DDDDDD",
}

# Semantic aliases
C_SA   = OI["blue"]     # single-agent
C_MAS  = OI["vermil"]   # multi-agent
C_OK   = OI["green"]    # satisfied
C_TRIG = OI["orange"]   # triggered / fired
C_NA   = OI["lgrey"]    # not configured / n/a

Target = Literal["paper", "slides", "web"]

# ---------------------------------------------------------------------------
# acmart column widths (inches)
# ---------------------------------------------------------------------------
PAPER_COL1 = 3.33    # single column
PAPER_COL2 = 6.97    # two columns


# ---------------------------------------------------------------------------
# SEM helper
# ---------------------------------------------------------------------------

def sem(p: float, n: int) -> float:
    """Standard error of the mean for a binary proportion.

    Parameters
    ----------
    p : float
        Observed proportion (0–1).
    n : int
        Number of independent observations.
    """
    return math.sqrt(p * (1.0 - p) / n) if 0 < p < 1 else 0.0


# ---------------------------------------------------------------------------
# Theme presets
# ---------------------------------------------------------------------------

def theme_mas(target: Target = "paper"):
    """Return the standard MAS-Lab plotnine theme for *target*.

    Parameters
    ----------
    target : {"paper", "slides", "web"}
        Rendering context.

    Returns
    -------
    plotnine.theme
        A composed theme ready to be added to any ``ggplot`` object.

    Examples
    --------
    ::

        from plotnine import ggplot, aes, geom_point
        from mas.lab.plots.mas_plot import theme_mas

        p = ggplot(df, aes("x", "y")) + geom_point() + theme_mas("slides")
    """
    from plotnine import (
        theme_minimal, theme,
        element_text, element_blank, element_line, element_rect,
    )

    _SIZES: dict[str, dict[str, float]] = {
        "paper":  {"base": 8.0,  "title": 9.0,  "axis": 7.5, "legend": 7.0, "strip": 7.5},
        "slides": {"base": 14.0, "title": 16.0, "axis": 13.0, "legend": 12.0, "strip": 13.0},
        "web":    {"base": 11.0, "title": 12.5, "axis": 10.0, "legend": 9.5,  "strip": 10.0},
    }
    s = _SIZES[target]

    return (
        theme_minimal() +
        theme(
            text=element_text(family="sans-serif", size=s["base"]),
            plot_title=element_text(size=s["title"]),
            axis_title=element_text(size=s["axis"]),
            axis_text=element_text(size=s["axis"] - 0.5),
            legend_text=element_text(size=s["legend"]),
            legend_title=element_text(size=s["legend"]),
            panel_grid_major=element_line(color="#cccccc", size=0.4),
            panel_grid_minor=element_blank(),
            panel_background=element_rect(fill="white", color="none"),
            plot_background=element_rect(fill="white", color="none"),
            legend_background=element_blank(),
            legend_box_background=element_blank(),
            strip_background=element_rect(fill="#f4f4f4", color="#cccccc"),
            strip_text=element_text(size=s["strip"]),
        )
    )


def theme_mas_paper():
    """Shorthand for ``theme_mas("paper")``."""
    return theme_mas("paper")


def theme_mas_slides():
    """Shorthand for ``theme_mas("slides")``."""
    return theme_mas("slides")


def theme_mas_web():
    """Shorthand for ``theme_mas("web")``."""
    return theme_mas("web")


# ---------------------------------------------------------------------------
# Scale helpers
# ---------------------------------------------------------------------------

def scale_fill_oi(values: dict[str, str] | list[str] | None = None, **kwargs):
    """``scale_fill_manual`` pre-loaded with the Okabe–Ito palette.

    Parameters
    ----------
    values : dict or list, optional
        Custom mapping (name → hex) or ordered list.  Defaults to the full
        eight-color Okabe–Ito sequence.
    """
    from plotnine import scale_fill_manual
    v: Any = values or OI_COLORS
    return scale_fill_manual(values=v, **kwargs)


def scale_color_oi(values: dict[str, str] | list[str] | None = None, **kwargs):
    """``scale_color_manual`` pre-loaded with the Okabe–Ito palette."""
    from plotnine import scale_color_manual
    v: Any = values or OI_COLORS
    return scale_color_manual(values=v, **kwargs)


def scale_linetype_mas(**kwargs):
    """``scale_linetype_manual`` with solid/dashed for single/multi patterns."""
    from plotnine import scale_linetype_manual
    return scale_linetype_manual(values={"single": "solid", "multi": "dashed"}, **kwargs)


# ---------------------------------------------------------------------------
# MASPlot base class
# ---------------------------------------------------------------------------

class MASPlot(ABC):
    """Grammar-of-graphics base class for all MAS-Lab scientific figures.

    Subclass this and implement :meth:`build` to return a fully-composed
    ``ggplot`` object.  The base class provides:

    * Three rendering targets (paper / slides / web) with matching themes
    * Okabe–Ito scale helpers via :meth:`scale_fill` and :meth:`scale_color`
    * Multi-format save (SVG + PDF by default) via :meth:`save`
    * Jupyter display via :meth:`show` / ``_repr_svg_``
    * One-liner pipeline wrapping via :meth:`to_step`

    Class attributes
    ----------------
    plot_type : str
        Short snake-case identifier (used as default file stem).
    default_width : float
        Figure width in inches (default: 3.33 — single acmart column).
    default_height : float
        Figure height in inches.
    default_formats : list[str]
        Output formats for :meth:`save` (default: ``["svg", "pdf"]``).
    default_target : Target
        Default rendering context (default: ``"paper"``).
    """

    plot_type:       str       = "mas_plot"
    default_width:   float     = PAPER_COL1
    default_height:  float     = 2.5
    default_formats: list[str] = ["svg", "pdf"]
    default_target:  Target    = "paper"

    def __init__(
        self,
        data: Any | None = None,
        *,
        target: Target | None = None,
        title: str | None = None,
        subtitle: str | None = None,
        width: float | None = None,
        height: float | None = None,
    ) -> None:
        self.data     = data
        self.target:  Target = target or self.default_target
        self._title    = title
        self._subtitle = subtitle
        self._width    = width  or self.default_width
        self._height   = height or self.default_height

    # ------------------------------------------------------------------
    # Abstract interface
    # ------------------------------------------------------------------

    @abstractmethod
    def build(self, data: Any = None) -> "ggplot":
        """Return a fully-composed plotnine ``ggplot`` object.

        Implementations should:

        * Accept *data* as the primary data source, falling back to
          ``self.data`` when *data* is ``None``.
        * Call ``self.theme()`` for the correct theme preset.
        * Use ``self.oi`` or :func:`scale_fill_oi` / :func:`scale_color_oi`
          for colorblind-safe colours.

        Parameters
        ----------
        data : any, optional
            Override ``self.data``.

        Returns
        -------
        plotnine.ggplot
            A ready-to-save ggplot object.
        """

    # ------------------------------------------------------------------
    # Theme / palette helpers (available in subclasses)
    # ------------------------------------------------------------------

    def theme(self):
        """Return the standard MAS-Lab theme for ``self.target``."""
        return theme_mas(self.target)

    def scale_fill(self, values: dict | list | None = None, **kw):
        """``scale_fill_manual`` with Okabe–Ito defaults."""
        return scale_fill_oi(values=values, **kw)

    def scale_color(self, values: dict | list | None = None, **kw):
        """``scale_color_manual`` with Okabe–Ito defaults."""
        return scale_color_oi(values=values, **kw)

    @property
    def oi(self) -> dict[str, str]:
        """Shorthand access to the Okabe–Ito name → hex dict."""
        return OI

    # ------------------------------------------------------------------
    # Save
    # ------------------------------------------------------------------

    def save(
        self,
        out_dir: Path | str,
        stem: str | None = None,
        data: Any = None,
        formats: list[str] | None = None,
        width: float | None = None,
        height: float | None = None,
    ) -> list[Path]:
        """Build and save the figure in one or more formats.

        Parameters
        ----------
        out_dir : Path or str
            Destination directory (created if it does not exist).
        stem : str, optional
            File name without extension (default: ``plot_type``).
        data : any, optional
            Override ``self.data``.
        formats : list[str], optional
            Formats to produce (default: ``default_formats``).
        width, height : float, optional
            Override figure dimensions in inches.

        Returns
        -------
        list[Path]
            Absolute paths of all written files.
        """
        out_dir = Path(out_dir)
        out_dir.mkdir(parents=True, exist_ok=True)

        effective_data    = data if data is not None else self.data
        effective_formats = formats or self.default_formats
        w = width  or self._width
        h = height or self._height
        stem = stem or self.plot_type

        p = self.build(effective_data)

        paths: list[Path] = []
        for fmt in effective_formats:
            out_path = out_dir / f"{stem}.{fmt}"
            with warnings.catch_warnings():
                warnings.simplefilter("ignore")
                p.save(
                    str(out_path),
                    width=w, height=h, units="in",
                    dpi=300, verbose=False,
                )
            paths.append(out_path)
        return paths

    # ------------------------------------------------------------------
    # Notebook / interactive display
    # ------------------------------------------------------------------

    def show(self, data: Any = None) -> None:
        """Draw inline in a Jupyter notebook."""
        effective_data = data if data is not None else self.data
        p = self.build(effective_data)
        p.draw()

    def _repr_svg_(self) -> str:
        """Return SVG string for Jupyter/IPython auto-display."""
        import tempfile
        p = self.build()
        with tempfile.NamedTemporaryFile(suffix=".svg", delete=False) as f:
            tmp = Path(f.name)
        with warnings.catch_warnings():
            warnings.simplefilter("ignore")
            p.save(
                str(tmp),
                width=self._width, height=self._height, units="in",
                dpi=150, verbose=False,
            )
        svg_content = tmp.read_text(encoding="utf-8")
        tmp.unlink(missing_ok=True)
        return svg_content

    def _repr_html_(self) -> str:
        """Fallback HTML display (base64-encoded SVG image)."""
        import base64
        svg = self._repr_svg_()
        b64 = base64.b64encode(svg.encode()).decode()
        return (
            f'<img src="data:image/svg+xml;base64,{b64}" '
            f'style="max-width:100%;height:auto">'
        )

    # ------------------------------------------------------------------
    # Pipeline step wrapping
    # ------------------------------------------------------------------

    def to_step(
        self,
        name: str | None = None,
        depends_on: list[str] | None = None,
        formats: list[str] | None = None,
        stem: str | None = None,
    ) -> "PlotPipelineStep":
        """Return a :class:`~mas.library.lab.steps.viz.plot_ggplot.PlotPipelineStep`
        wrapping this plot.

        The step calls :meth:`save` when executed, writing output files
        to the pipeline's output directory.

        Parameters
        ----------
        name : str, optional
            Step name in the pipeline (default: class name).
        depends_on : list[str], optional
            Upstream step names to wait for.
        formats : list[str], optional
            Override ``default_formats``.
        stem : str, optional
            Override output file stem.

        Examples
        --------
        ::

            pipeline = [
                analysis_step,
                my_plot.to_step("quality_bar", depends_on=["analysis"]),
            ]
        """
        from mas.library.lab.steps.viz.plot_ggplot import PlotPipelineStep

        return PlotPipelineStep(
            name=name or type(self).__name__,
            config={
                "formats": formats or self.default_formats,
                "stem":    stem    or self.plot_type,
            },
            depends_on=depends_on or [],
            plot=self,
        )

    def __repr__(self) -> str:
        return (
            f"{type(self).__name__}("
            f"target={self.target!r}, "
            f"{self._width:.2f}×{self._height:.2f}in, "
            f"formats={self.default_formats!r})"
        )


